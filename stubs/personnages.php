						<p>Selon des textes sacrés inconnus des mortels, les Ornyx seraient les premières âmes à s’être éveillées à la Conscience, sous l’influence du roi Osiris, souverain de la première dynastie d’Égypte, six mille ans avant J.C. Osiris prétendait qu’ils auraient pour fonction de maintenir une sorte d’équilibre entre la Source Atmique, le Plan Astral et le plan Physique, et que leur pouvoir mis en commun serait capable d’ouvrir la Source Atmique pour provoquer un Big Bang et renouveler tout l’Univers.</p>
						<p>Quoi qu’il en soit, les Ornyx ont évolué individuellement en progressant chacun dans un domaine particulier, complémentaire à celui des autres Ornyx. Séparés, ils sont des êtres supérieurs, mais faillibles comme les mortels. Réunis, ils possèdent l’Intelligence Infinie, capable de résoudre n’importe quelle crise.</p>
